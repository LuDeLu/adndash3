// Función para obtener estadísticas del proyecto
export const getDomeProjectStats = () => {
  const allApartments = [
    ...domePlantaBajaData,
    ...domeFloorsData.flatMap((floor) => [...floor.sections.A, ...floor.sections.B, ...floor.sections.C]),
  ]

  const totalUnits = allApartments.length
  const availableUnits = allApartments.filter((apt) => apt.status === "DISPONIBLE").length
  const soldUnits = allApartments.filter((apt) => apt.status === "VENDIDO").length
  const reservedUnits = allApartments.filter((apt) => apt.status === "reservado").length

  return {
    totalUnits,
    availableUnits,
    soldUnits,
    reservedUnits,
    occupancyRate: Math.round(((soldUnits + reservedUnits) / totalUnits) * 100),
  }
}

// Función para obtener apartamento por ID
export const getDomeApartmentById = (id: string): DomeApartment | null => {
  // Buscar en planta baja
  const pbApartment = domePlantaBajaData.find((apt) => apt.id === id)
  if (pbApartment) return pbApartment

  // Buscar en pisos
  for (const floor of domeFloorsData) {
    const apartment = [...floor.sections.A, ...floor.sections.B, ...floor.sections.C].find((apt) => apt.id === id)

    if (apartment) return apartment
  }

  return null
}

// Función para actualizar estado de apartamento
export const updateDomeApartmentStatus = (id: string, newStatus: DomeApartment["status"]): boolean => {
  // Buscar en planta baja
  const pbIndex = domePlantaBajaData.findIndex((apt) => apt.id === id)
  if (pbIndex !== -1) {
    domePlantaBajaData[pbIndex].status = newStatus
    return true
  }

  // Buscar en pisos
  for (const floor of domeFloorsData) {
    const sections = ["A", "B", "C"] as const
    for (const sectionKey of sections) {
      const section = floor.sections[sectionKey]
      const aptIndex = section.findIndex((apt) => apt.id === id)
      if (aptIndex !== -1) {
        section[aptIndex].status = newStatus
        return true
      }
    }
  }

  return false
}
